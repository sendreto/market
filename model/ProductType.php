<?php


class ProductType
{

    private int $id;
    private string $name;
    private float $tax;

    /**
     * ProductType constructor.
     * @param string $name
     * @param float $tax
     */
    public function __construct(string $name, float $tax)
    {
        $this->name = $name;
        $this->tax = $tax;
    }


    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    /**
     * @return float
     */
    public function getTax(): float
    {
        return $this->tax;
    }

    /**
     * @param float $tax
     */
    public function setTax(float $tax): void
    {
        $this->tax = $tax;
    }

}
